package com.performanceanalyst.model;

public class BatchDetails {
	private int centerCourseId,courseMonth,courseYear,courseDuration;

	public int getCenterCourseId() {
		return centerCourseId;
	}

	public void setCenterCourseId(int centerCourseId) {
		this.centerCourseId = centerCourseId;
	}

	public int getCourseMonth() {
		return courseMonth;
	}

	public void setCourseMonth(int courseMonth) {
		this.courseMonth = courseMonth;
	}

	public int getCourseYear() {
		return courseYear;
	}

	public void setCourseYear(int courseYear) {
		this.courseYear = courseYear;
	}

	public int getCourseDuration() {
		return courseDuration;
	}

	public void setCourseDuration(int courseDuration) {
		this.courseDuration = courseDuration;
	}
}
